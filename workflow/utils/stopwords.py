def is_stopword(word: str) -> bool:
    """불용어 여부 확인

    Args:
        word: 확인할 단어

    Returns:
        불용어이면 True
    """
    return word.lower() in DOMAIN_STOPWORDS or word in DOMAIN_STOPWORDS


def filter_stopwords(words: list) -> list:
    """불용어 필터링

    Args:
        words: 단어 목록

    Returns:
        불용어가 제거된 단어 목록
    """
    return [w for w in words if not is_stopword(w)]
